package com.example.goalaso;
public class Paging {

    private float current;
    private float total;


    // Getter Methods

    public float getCurrent() {
        return current;
    }

    public float getTotal() {
        return total;
    }

    // Setter Methods

    public void setCurrent(float current) {
        this.current = current;
    }

    public void setTotal(float total) {
        this.total = total;
    }
}
